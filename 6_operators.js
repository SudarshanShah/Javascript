// Arithmetic Operators
let a = 45;
let b = 4;

console.log("a + b ", a+b);
console.log("a - b ", a-b);
console.log("a * b ", a*b);
console.log("a ** b ", a**b);
console.log("a / b ", a/b);
console.log("a % b ", a%b);
console.log("a++ ", a++);
console.log("a-- ", a--);


// Assignement Operators
let n = 10; // assignment of value 
n += 5; // same as n = n + 5
n -= 5; // same as n = n - 5
n *= 5; // same as n = n * 5
n /= 5; // same as n = n / 5
n %= 5; // same as n = n % 5
n **= 5; // same as n = n ** 5


// Comparison Operators
let comp1 = 6;
let comp2 = '6';
console.log("comp1 == comp2 is ", comp1 == comp2); // it just checks if value is equal
console.log("comp1 != comp2 is ", comp1 != comp2);
console.log("comp1 === comp2 is ", comp1 === comp2); // it checks if value and data type are equal
console.log("comp1 !== comp2 is ", comp1 !== comp2);


// Logical Operators
